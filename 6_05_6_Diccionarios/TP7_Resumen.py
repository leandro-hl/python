# TP 7 - Recursividad
# Una función recursiva es aquella que, dentro de la misma, vuelve a llamarse a sí misma en un dominio inferior.

# El cuerpo de la función consta de al menos dos partes significativas que son:
# CASO BASE: o condición de salida, donde finaliza la recursividad.
# CASO RECURSIVO: llamada a la función con un dominio inferior.

# IMPORTANTE: Si la funcion no posee o no logra ejecutar nunca el caso base, jamás termina. El programa
# finalizará cancelando, porque la computadora se quedará sin memoria suficiente.

